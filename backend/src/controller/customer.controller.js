const Customer = require('../models/Customer');

// POST /api/customers
// Called when a customer submits their details at checkout or landing page.
// Does three things in order:
//   1. Check if phone already exists (no duplicate customers)
//   2. If a referral code was entered, find the owner
//   3. Save the new customer with all resolved fields

exports.createCustomer = async (req, res, next) => {
  try {
    const { name, phone, email, referralCode } = req.body;

    // ── Validate required fields ─────────────────────────────────────────
    if (!name || !phone) {
      return res.status(400).json({
        success: false,
        message: 'Name and phone are required'
      });
    }

    // ── Check if this phone number is already registered ─────────────────
    // WHY: prevent duplicate customer records for the same person
    const existing = await Customer.findOne({ phone });
    if (existing) {
      return res.status(200).json({
        success:  true,
        message:  'Welcome back!',
        customer: existing,
        isNew:    false,
      });
    }

    // ── Resolve the referral code if one was provided ────────────────────
    let referralValid       = false;
    let referredByCustomer  = null;

    if (referralCode && referralCode.trim() !== '') {
      // Search for ANY customer whose myReferralCode matches what was entered
      const referrer = await Customer.findOne({
        myReferralCode: referralCode.trim().toUpperCase()
      });

      if (referrer) {
        // Code is valid — record who referred this customer
        referralValid      = true;
        referredByCustomer = referrer._id;
      }
      // If referrer is null, the code was entered but doesn't match anyone.
      // referralValid stays false. We still save the customer — bad code
      // is not a reason to block the booking.
    }

    // ── Create the new customer ───────────────────────────────────────────
    // myReferralCode is auto-generated by the schema default function.
    const customer = await Customer.create({
      name,
      phone,
      email:              email || null,
      referredBy:         referralCode ? referralCode.trim().toUpperCase() : null,
      referralValid,
      referredByCustomer,
    });

    // ── Build the response ────────────────────────────────────────────────
    // WHY different messages: gives the frontend something useful to show.
    const message = referralCode
      ? referralValid
        ? `Welcome, ${name}! Referral code accepted ✓`
        : `Welcome, ${name}! (Referral code not recognised)`
      : `Welcome, ${name}!`;

    return res.status(201).json({
      success:       true,
      message,
      isNew:         true,
      referralValid,
      customer: {
        _id:            customer._id,
        name:           customer.name,
        phone:          customer.phone,
        myReferralCode: customer.myReferralCode, // give them their own code
        referralValid,
      },
    });

  } catch (err) { next(err); }
};


// GET /api/customers/verify-referral?code=YC-K7MR
// Called in real-time as the user types their referral code in the frontend.
// Returns whether the code is valid without creating anything.

exports.verifyReferral = async (req, res, next) => {
  try {
    const { code } = req.query;

    if (!code || code.trim() === '') {
      return res.status(400).json({ success: false, message: 'No code provided' });
    }

    const referrer = await Customer.findOne({
      myReferralCode: code.trim().toUpperCase()
    }).select('name myReferralCode');
    // WHY .select(): only return name and code — not phone or email of referrer

    if (!referrer) {
      return res.json({ success: true, valid: false, message: 'Code not found' });
    }

    return res.json({
      success: true,
      valid:   true,
      message: `Referred by ${referrer.name}`,
    });

  } catch (err) { next(err); }
};
